//*****************************************************************************
//   静的メンバー変数宣言
//*****************************************************************************
CShaderModel *CShaderManager::m_pModel = NULL;                      // レンダラー

//==================================================================================================================================================
// 初期化処理
//==================================================================================================================================================
void CShaderManager::Init()
{
	m_pModel = new CShaderModel;
	m_pModel->Create();

}

//==================================================================================================================================================
// 終了処理
//==================================================================================================================================================
void CShaderManager::Uninit(void)
{
	if (m_pModel != NULL) { 
		m_pModel->Delete();
		m_pModel = NULL;
	}
}
